package com.lti.model;

public class Account {

	int accName;
	int balance;
	public int getAccName() {
		return accName;
	}
	public void setAccName(int accName) {
		this.accName = accName;
	}
	public int getBalance() {
		return balance;
	}
	public void setBalance(int balance) {
		this.balance = balance;
	}
	
}
